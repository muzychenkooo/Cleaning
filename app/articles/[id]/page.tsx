import { site, baseUrl } from '@/data/site';
import { completedReports } from '@/data/reports';

type PageProps = {
  params: Promise<{ id: string }>;
};

const ARTICLE_FALLBACK_TITLE = 'Выполненная работа';

function getReportById(id: string) {
  return completedReports.find((report) => report.id === id);
}

function getArticleMetaDescription(title: string) {
  return `${title} — подробный кейс по профессиональной уборке от клининговой компании ${site.name} в Москве и Московской области.`;
}

export async function generateStaticParams() {
  return completedReports.map((report) => ({ id: report.id }));
}

export async function generateMetadata({ params }: PageProps): Promise<Metadata> {
  const { id } = await params;
  const report = getReportById(id);
  const h1Title = report?.title ?? ARTICLE_FALLBACK_TITLE;
  const pageTitle = `${h1Title} | ${site.name}`;
  const url = `${baseUrl}/articles/${id}/`;
  const description = getArticleMetaDescription(h1Title);

  return {
    title: pageTitle,
    description,
    openGraph: {
      title: pageTitle,
      description,
      url,
      locale: 'ru_RU',
    },
    alternates: { canonical: url },
  };
}

